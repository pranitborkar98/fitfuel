async function seedMealPlans() {
  console.log('🌱 Starting meal plan seed...')
  console.log(`📋 Total plans to seed: ${plans.length}`)

  let upserted = 0
  let failed = 0
  let priceRows = 0

  for (const plan of plans) {
    try {
      // 1. Upsert MealPlan row
      const saved = await prisma.mealPlan.upsert({
        where: { slug: plan.slug },
        update: plan,
        create: plan,
      })

      // 2. Seed PlanPrice rows linked via mealPlanId
      const multiplier = PRICE_MULTIPLIER[plan.category] ?? 1.0
      const dietType   = DIET_TYPE_MAP[plan.dietaryVariant] ?? 'VEGETARIAN'

      for (const base of BASE_PRICES) {
        const finalPrice = roundPrice(base.priceRs * multiplier)
        try {
          await prisma.planPrice.upsert({
            where: {
              mealPlanId_duration_mealsPerDay: {
                mealPlanId:  saved.id,
                duration:    base.duration,
                mealsPerDay: base.mealsPerDay,
              },
            },
            create: {
              mealPlanId:  saved.id,
              diet:        dietType,
              duration:    base.duration,
              mealsPerDay: base.mealsPerDay,
              priceRs:     finalPrice,
              gstPercent:  5,
              isActive:    true,
            },
            update: {
              priceRs:  finalPrice,
              diet:     dietType,
              isActive: true,
            },
          })
          priceRows++
        } catch (priceErr: any) {
          if (priceErr.code !== 'P2002') throw priceErr
        }
      }

      const mul = multiplier > 1 ? ` (${multiplier}x)` : ''
      console.log(`✅ ${plan.displayName}${mul}`)
      upserted++
    } catch (err) {
      console.error(`❌ Failed: ${plan.displayName}`, err)
      failed++
    }
  }

  console.log('\n─────────────────────────────────────────────────────')
  console.log(`✅ Plans upserted : ${upserted}`)
  console.log(`❌ Plans failed   : ${failed}`)
  console.log(`💰 Price rows     : ${priceRows}`)
  console.log(`📋 Total plans    : ${plans.length}`)
  console.log('─────────────────────────────────────────────────────')

  console.log('\n📊 Breakdown by category:')
  const byCat: Record<string, number> = {}
  for (const p of plans) { byCat[p.category] = (byCat[p.category] || 0) + 1 }
  for (const [cat, count] of Object.entries(byCat)) {
    const mul = PRICE_MULTIPLIER[cat] ?? 1.0
    console.log(`   ${cat}: ${count} plans × 21 prices @ ${mul}x = ${count * 21} rows`)
  }

  console.log('\n📊 Breakdown by diet variant:')
  const byDiet: Record<string, number> = {}
  for (const p of plans) { byDiet[p.dietaryVariant] = (byDiet[p.dietaryVariant] || 0) + 1 }
  for (const [diet, count] of Object.entries(byDiet)) {
    console.log(`   ${diet}: ${count} plans`)
  }

  console.log('\n⚠️  isActive is managed manually — weight-loss-veg stays live.')
  console.log('   To activate: UPDATE meal_plans SET is_active = true WHERE slug = \'your-slug\';')
  console.log('\n🎉 Seed complete!')
}

// ---------------------------------------------------------------------------
// RUN
// ---------------------------------------------------------------------------

seedMealPlans()
  .catch((e) => {
    console.error('💥 Seed failed:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
    await pool.end()
  })