// ---------------------------------------------------------------------------
// SEED EXECUTION
// ---------------------------------------------------------------------------

async function main() {
  console.log('🥚 Muscle Gain Egg Recipe Seed — starting...')

  // 1. Create missing FoodItems
  console.log(`Creating ${MISSING_FOOD_ITEMS.length} missing FoodItems...`)
  for (const item of MISSING_FOOD_ITEMS) {
    const slug = item.name
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, '-')
      .replace(/-+/g, '-')
      .replace(/^-|-$/g, '')

    const existing = await prisma.foodItem.findFirst({
      where: { OR: [{ name: item.name }, { slug }] },
    })

    if (existing) {
      FOOD_ITEM_MAP[slug.toUpperCase().replace(/-/g, '_')] = existing.id
      console.log(`  ✓ FoodItem already exists: ${item.name} (${existing.id})`)
      continue
    }

    const created = await prisma.foodItem.create({
      data: {
        name: item.name,
        slug,
        category: item.category,
        per100Calories: item.per100Calories,
        per100Protein: item.per100Protein,
        per100Carbs: item.per100Carbs,
        per100Fat: item.per100Fat,
        per100Fiber: item.per100Fiber,
        isActive: true,
      },
    })

    const key = slug.toUpperCase().replace(/-/g, '_')
    FOOD_ITEM_MAP[key] = created.id
    console.log(`  ✓ Created FoodItem: ${item.name} (${created.id})`)
  }

  // 2. Find the Muscle Gain Egg meal plan
  const mealPlan = await prisma.mealPlan.findFirst({
    where: {
      slug: 'muscle-gain-egg',
      isActive: true,
    },
  })

  if (!mealPlan) {
    console.warn('⚠️  Meal plan "muscle-gain-egg" not found. Recipes will be created but not linked.')
  } else {
    console.log(`🔗 Found meal plan: ${mealPlan.name} (${mealPlan.id})`)
  }

  // 3. Upsert recipes
  let createdCount = 0
  let updatedCount = 0

  for (const def of RECIPES) {
    const { recipe, ingredients, steps } = def

    const existingRecipe = await prisma.recipe.findUnique({
      where: { slug: recipe.slug },
    })

    const recipeData = {
      name: recipe.name,
      slug: recipe.slug,
      description: recipe.description,
      shortDescription: recipe.shortDescription,
      cuisineType: recipe.cuisineType,
      mealType: recipe.mealType,
      dietaryTags: recipe.dietaryTags,
      planCategories: recipe.planCategories,
      tierAvailability: recipe.tierAvailability,
      servingSizeGrams: recipe.servingSizeGrams,
      prepTimeMins: recipe.prepTimeMins,
      cookTimeMins: recipe.cookTimeMins,
      difficulty: recipe.difficulty,
      equipmentNeeded: recipe.equipmentNeeded,
      allergens: recipe.allergens,
      shelfLifeHours: recipe.shelfLifeHours,
      packagingType: recipe.packagingType,
      kitchenStation: recipe.kitchenStation,
      seasonTags: recipe.seasonTags,
      rotationGroup: recipe.rotationGroup,
      caloriesPerServing: recipe.caloriesPerServing,
      proteinGrams: recipe.proteinGrams,
      carbsGrams: recipe.carbsGrams,
      fatGrams: recipe.fatGrams,
      fibreGrams: recipe.fibreGrams,
      caloriesPer100g: recipe.caloriesPer100g,
      proteinPer100g: recipe.proteinPer100g,
      carbsPer100g: recipe.carbsPer100g,
      fatPer100g: recipe.fatPer100g,
      fibrePer100g: recipe.fibrePer100g,
      glycaemicIndex: recipe.glycaemicIndex ?? null,
      isActive: recipe.isActive,
      isFeatured: recipe.isFeatured,
    }

    let recipeRecord

    if (existingRecipe) {
      recipeRecord = await prisma.recipe.update({
        where: { slug: recipe.slug },
        data: recipeData,
      })
      updatedCount++
      console.log(`  ↻ Updated recipe: ${recipe.name}`)
    } else {
      recipeRecord = await prisma.recipe.create({
        data: recipeData,
      })
      createdCount++
      console.log(`  ✓ Created recipe: ${recipe.name}`)
    }

    // Delete existing ingredients and steps to rebuild
    await prisma.recipeIngredient.deleteMany({ where: { recipeId: recipeRecord.id } })
    await prisma.recipeStep.deleteMany({ where: { recipeId: recipeRecord.id } })

    // Create ingredients
    for (const ing of ingredients) {
      const foodItemId = getFoodItemId(ing.foodItemKey)
      await prisma.recipeIngredient.create({
        data: {
          recipeId: recipeRecord.id,
          foodItemId,
          quantityGrams: ing.quantityGrams,
          cookedWeightFactor: ing.cookedWeightFactor,
          prepNote: ing.prepNote ?? null,
          isOptional: ing.isOptional,
          orderInRecipe: ing.orderInRecipe,
        },
      })
    }

    // Create steps
    for (const step of steps) {
      await prisma.recipeStep.create({
        data: {
          recipeId: recipeRecord.id,
          stepNumber: step.stepNumber,
          title: step.title,
          instruction: step.instruction,
          durationMins: step.durationMins ?? null,
          temperatureC: step.temperatureC ?? null,
          technique: step.technique ?? null,
          kitchenNote: step.kitchenNote ?? null,
        },
      })
    }

    // Link to meal plan if found
    if (mealPlan) {
      const existingLink = await prisma.mealPlanRecipe.findFirst({
        where: {
          mealPlanId: mealPlan.id,
          recipeId: recipeRecord.id,
        },
      })

      if (!existingLink) {
        await prisma.mealPlanRecipe.create({
          data: {
            mealPlanId: mealPlan.id,
            recipeId: recipeRecord.id,
            isDefault: true,
            isActive: true,
          },
        })
        console.log(`    → Linked to meal plan: ${mealPlan.name}`)
      }
    }
  }

  console.log(`\n✅ Seed complete: ${createdCount} created, ${updatedCount} updated, ${RECIPES.length} total recipes.`)
  console.log('   All recipes tagged for: muscle_gain | EGG | VEGETARIAN or NON_VEG')
}

main()
  .catch((e) => {
    console.error('❌ Seed failed:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
